        <h5 class="center-text">Usability Testing</h5>

        <p>Before I built a prototype I wanted to know what users thought of the existing application. I designed a script to moderate users through specific actions to gain critical insight on their thought process and observations. I performed in-person and remote tests to get a diverse set of users. The main thing I wanted to evaluate was if users were able to complete a budget and understand what impact it had.</p>
        <br>
