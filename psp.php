        <h5 class="center-text">Heuristic Analysis</h5>

        <p>I took the budgeting application and created heuristics similar to Jakob Nielson's to evaluate problem areas. Since there were no instructions for the application, I had to use my best judgement to describe the actions I was performing. I then took my results and created a report to highlight areas that needed improvement.</p>
