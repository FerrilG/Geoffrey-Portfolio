            <p>Prime currently uses a portal for the application process. I also suggested that they utilize this to gather additional metrics on who is using their site. Items that could be on this enhanced portal include a campus calendar, a blog section, links to continued learning, and the ability to ask students questions under the monitoring of student staff.</p>
            <p>Blog posts could include student testimonies, Prime's commitment to students, How Prime builds professional development, and various items</p>
            <p>Continued Learning could circle back to Prime's events or links to help promote learning whether that's through partners or events.</p>
