        <h2 class="center-text">Proposed Solution</h2>
        <p>Now that we knew what Prime wanted to be, we could formulate a way to get that message out to the public. We brainstormed and discovered four methods that would improve the visibility, message, commitment, and return on applications.</p>
        
        <!-- Social Media - Marketing -->
        <h5 class="center-text">Social Media - Marketing</h5>

        <p>We recommended that Prime use social media to reach a larger audience. It is a low-cost option to yield  visibility. The reason we stressed visibility is because there could be a large number of individuals who want to change industries but don't know how to do so without being in it.</p>
        <p>We suggested a type of campaign called "A Day In the Life" which is based on an Instagram-takeover in which a student posts for a day on everything they are doing. Future students could then relate to those enrolled and envision themselves in the shoes of the student. Gaining perspective on what a student does daily. Additionally this could then be shared and reach a broader audience across multiple platforms.</p>

        <div>
            <img class="center space" src="images/prime/A_Day_in_the_Life.png" alt="Social Media Storyboard">
        </div>

        <!-- Website - Restructure -->
        <h5 class="center-text">Website - Restructure</h5>

        <p>The current website has all the information that is required, however it was in a format that doesn't keep the reader interested. I informed them that, according to Jakob Nielson, the average page visit is less than a minute before readers move on. I used that to promote a website restructure that would help retain traffic.</p>
        <p>Begin with making readers desire a change. Explain to them what sets Prime apart. Now that they know what Prime is empathize with them and let them know you understand. Follow this up with current or past students to support your empathy. Lastly, let them know how to apply. This structure would help motivate individuals to want to learn more and get in contact with Prime.</p>
    
        <div>
            <img class="center space" src="images/prime/Website_Restructure.jpg" alt="Website Restructure">
        </div>

